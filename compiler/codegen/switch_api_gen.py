def _coverage_bin_for_field(struct_name: str, field_name: str, width: int) -> str:
    """Generate the coverage bin dict entry for a single field."""
    key = f"{struct_name}.{field_name}"
    if width == 1:
        return f'        **_b1("{key}"),'
    elif width == 2:
        return f'        **_b2("{key}"),'
    elif width == 3:
        return f'        **_b3("{key}"),'
    elif width == 4:
        return f'        **_b4("{key}"),'
    elif width <= 8:
        return f'        **_bm("{key}",{width}),'
    else:
        return f'        **_bw("{key}"),'


def generate_coverage_bins(reg_map) -> str:
    """Generate coverage_bins.py content with all register field bins.

    Returns a Python module string that coverage.py can import.
    """
    lines = []
    lines.append("# Auto-generated by 8m compiler — do not edit")
    lines.append("# Import this from coverage.py to get all register field bins.")
    lines.append("")
    lines.append("def _b1(n): return {f'{n}=0':0, f'{n}=1':0}")
    lines.append("def _b2(n): return {f'{n}={v}':0 for v in range(4)}")
    lines.append("def _b3(n): return {f'{n}={v}':0 for v in range(8)}")
    lines.append("def _b4(n): return {f'{n}={v}':0 for v in range(16)}")
    lines.append("def _bw(n): return {f'{n}=0':0, f'{n}=nonzero':0, f'{n}=max':0}")
    lines.append("def _bm(n,w): return {f'{n}=0':0, f'{n}=mid':0, f'{n}={2**w-1}':0}")
    lines.append("")
    lines.append("def get_register_bins():")
    lines.append('    """Return dict of {coverpoint_name: {bin_name: 0}} for all register fields."""')
    lines.append("    bins = {}")
    lines.append("")
    lines.append("    def A(n, d): bins[n] = d")
    lines.append("")

    for mt in reg_map.mem_tables:
        name = mt.name
        array_name = f"{name}_mem"
        lines.append(f"    # -- {array_name}[{mt.num_entries}] --")
        lines.append(f"    A('{array_name}', {{")
        for fld in mt.fields:
            if fld.name.startswith("__pad"):
                continue
            lines.append(_coverage_bin_for_field(array_name, fld.name, fld.width or 1))
        lines.append("    })")
        lines.append("")

    for reg in reg_map.registers:
        name = reg.name
        lines.append(f"    # -- {name} (singleton) --")
        lines.append(f"    A('{name}', {{")
        for fld in reg.fields:
            if fld.name.startswith("__pad"):
                continue
            lines.append(_coverage_bin_for_field(name, fld.name, fld.width or 1))
        lines.append("    })")
        lines.append("")

    # Prior maps from tinyReg2
    lines.append("    # -- Ds1qPriorMap_mem[8] --")
    lines.append("    A('Ds1qPriorMap_mem', {**_b2('Ds1qPriorMap_mem.prior')})")
    lines.append("")
    lines.append("    # -- DsDscpPriorMap_mem[64] --")
    lines.append("    A('DsDscpPriorMap_mem', {**_b2('DsDscpPriorMap_mem.prior')})")
    lines.append("")
    lines.append("    return bins")
    lines.append("")

    return "\n".join(lines)
